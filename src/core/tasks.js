/**
 * @param {string} projectRoot
 * @param {{title: string, description?: string, branches?: string[]}} input
 * @returns {Promise<Task>}
 */
export async function addTask(projectRoot, input) {
  const state = await readState(projectRoot);

  if (!input.title?.trim()) {
    throw new TaskStateError('Task title is required.');
  }
  const branches = input.branches ?? [];
  assertBranchesValid(branches, state.branches_index);

  const id = nextId(state.tasks);
  const now = new Date().toISOString();
  /** @type {Task} */
  const task = {
    id,
    title: input.title.trim(),
    description: (input.description ?? '').trim(),
    status: 'pending',
    branches: [...branches],
    created_at: now,
    updated_at: now,
    completed_at: null,
    notes: [],
  };

  state.tasks.push(task);
  await writeState(projectRoot, state);
  await regenerateTasksMd(projectRoot, state);
  return task;
}

/**
 * @param {string} projectRoot
 * @param {string} id
 * @param {TaskStatus} newStatus
 * @returns {Promise<{task: Task, shouldRefresh: boolean}>}
 */
export async function setStatus(projectRoot, id, newStatus) {
  if (!VALID_STATUSES.includes(newStatus)) {
    throw new TaskStateError(
      `Invalid status '${newStatus}'. Valid: ${VALID_STATUSES.join(', ')}.`,
    );
  }

  const state = await readState(projectRoot);
  const task = state.tasks.find((t) => t.id === id);
  if (!task) throw new TaskStateError(`Task ${id} not found.`);

  const wasDone = task.status === 'done';
  const nowDone = newStatus === 'done';

  task.status = newStatus;
  task.updated_at = new Date().toISOString();
  task.completed_at = nowDone ? task.updated_at : null;

  // Counter logic: only increment when we TRANSITION into done.
  let shouldRefresh = false;
  if (!wasDone && nowDone) {
    state.counters.done_since_refresh += 1;
    if (state.counters.done_since_refresh >= state.counters.auto_refresh_threshold) {
      shouldRefresh = true;
      // Note: we don't reset the counter here — that's the refresh command's
      // job, after it successfully regenerates compact files. If the refresh
      // fails, the counter stays high and the user gets prompted again.
    }
  }

  await writeState(projectRoot, state);
  await regenerateTasksMd(projectRoot, state);
  return { task, shouldRefresh };
}

/**
 * @param {string} projectRoot
 * @param {string} id
 * @param {string} content
 */
export async function addNote(projectRoot, id, content) {
  const state = await readState(projectRoot);
  const task = state.tasks.find((t) => t.id === id);
  if (!task) throw new TaskStateError(`Task ${id} not found.`);
  task.notes.push({ at: new Date().toISOString(), content: content.trim() });
  task.updated_at = new Date().toISOString();
  await writeState(projectRoot, state);
}

/**
 * Called by the refresh command after compact files were regenerated
 * successfully. Resets the counter atomically.
 *
 * @param {string} projectRoot
 */
export async function markRefreshed(projectRoot) {
  const state = await readState(projectRoot);
  state.counters.done_since_refresh = 0;
  state.counters.last_refresh_at = new Date().toISOString();
  await writeState(projectRoot, state);
}

/**
 * @param {string} projectRoot
 * @param {{status?: TaskStatus, branch?: string}} [filter]
 */
export async function listTasks(projectRoot, filter = {}) {
  const state = await readState(projectRoot);
  let tasks = state.tasks;
  if (filter.status) tasks = tasks.filter((t) => t.status === filter.status);
  if (filter.branch) tasks = tasks.filter((t) => t.branches.includes(filter.branch));
  return tasks;
}

