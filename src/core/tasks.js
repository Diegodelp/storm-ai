export class TaskStateError extends Error {
  constructor(message) {
    super(message);
    this.name = 'TaskStateError';
  }
}

/**
 * Ensure the state object has all expected fields with reasonable defaults.
 * Called on every read; makes older state files forward-compatible.
 *
 * @param {any} raw
 * @returns {TaskState}
 */
function normalizeState(raw) {
  const state = raw ?? {};
  state.version ??= 1;
  state.project ??= { name: 'unknown', created_at: new Date().toISOString() };
  state.counters ??= {};
  state.counters.done_since_refresh ??= 0;
  state.counters.auto_refresh_threshold ??= DEFAULT_THRESHOLD;
  state.counters.last_refresh_at ??= null;
  state.branches_index ??= [];
  state.tasks ??= [];

  for (const t of state.tasks) {
    t.branches ??= [];
    t.notes ??= [];
    t.completed_at ??= null;
    t.description ??= '';
  }

  return state;
}

/**
 * @param {Task[]} tasks
 * @returns {string} Next sequential ID, e.g. "T-017".
 */
function nextId(tasks) {
  let max = 0;
  for (const t of tasks) {
    const m = /^T-(\d+)$/.exec(t.id);
    if (m) max = Math.max(max, parseInt(m[1], 10));
  }
  return `T-${String(max + 1).padStart(3, '0')}`;
}

/**
 * @param {string[]} wanted
 * @param {string[]} index
 */
function assertBranchesValid(wanted, index) {
  if (wanted.length === 0) return;
  if (index.length === 0) return; // empty index = don't validate (fresh project)
  const bad = wanted.filter((b) => !index.includes(b));
  if (bad.length > 0) {
    throw new TaskStateError(
      `Unknown branch(es): ${bad.join(', ')}. Known branches: ${index.join(', ')}.`,
    );
  }
}
