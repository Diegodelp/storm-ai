/**
 * Task CRUD over task-state.json.
 *
 * Single source of truth for task data. TASKS.md is a VIEW generated from
 * this file — never edited directly.
 *
 * Design decisions:
 *   - IDs are sequential ("T-001", "T-002"...). Sequential IDs are easier
 *     to reference verbally than UUIDs ("finish T-003" vs "finish a8f3..").
 *   - `branches` on each task is validated against project.config.json at
 *     write time, so typos ("src/autth") fail fast.
 *   - Reading is cheap and called often; writing normalizes + validates.
 *   - All public functions take `projectRoot` and read/write fresh — no
 *     in-memory cache — because the user may edit files externally and
 *     we must not overwrite their changes with stale data.
 */

import { readFile, writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';

/** @typedef {'pending'|'in_progress'|'done'|'cancelled'} TaskStatus */

/**
 * @typedef {Object} Task
 * @property {string} id
 * @property {string} title
 * @property {string} description
 * @property {TaskStatus} status
 * @property {string[]} branches          Paths into branches_index.
 * @property {string} created_at          ISO 8601.
 * @property {string} updated_at          ISO 8601.
 * @property {string|null} completed_at   ISO 8601 or null.
 * @property {Array<{at: string, content: string}>} notes
 */

/**
 * @typedef {Object} TaskState
 * @property {number} version
 * @property {{name: string, created_at: string}} project
 * @property {{done_since_refresh: number, auto_refresh_threshold: number, last_refresh_at: string|null}} counters
 * @property {string[]} branches_index
 * @property {Task[]} tasks
 */

export const VALID_STATUSES = ['pending', 'in_progress', 'done', 'cancelled'];
const STATE_FILE = path.join('.context-compact', 'task-state.json');
const TASKS_FILE = 'TASKS.md';
const DEFAULT_THRESHOLD = 5;

// ---------------------------------------------------------------------------
// I/O helpers
// ---------------------------------------------------------------------------

/**
 * @param {string} projectRoot
 * @returns {Promise<TaskState>}
 */
export async function readState(projectRoot) {
  const p = path.join(projectRoot, STATE_FILE);
  let raw;
  try {
    raw = await readFile(p, 'utf8');
  } catch (err) {
    if (err.code === 'ENOENT') {
      throw new TaskStateError(
        `No task-state.json at ${p}. Is this a storm project? Run 'storm new' first.`,
      );
    }
    throw err;
  }

  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (err) {
    throw new TaskStateError(`task-state.json is not valid JSON: ${err.message}`);
  }

  return normalizeState(parsed);
}

/**
 * @param {string} projectRoot
 * @param {TaskState} state
 */
export async function writeState(projectRoot, state) {
  const p = path.join(projectRoot, STATE_FILE);
  await mkdir(path.dirname(p), { recursive: true });
  // UTF-8 WITHOUT BOM — node writeFile default already does this.
  await writeFile(p, JSON.stringify(state, null, 2) + '\n', 'utf8');
}

/**
 * Regenerate TASKS.md from the authoritative state.
 * Called after every mutation.
 *
 * @param {string} projectRoot
 * @param {TaskState} state
 */
export async function regenerateTasksMd(projectRoot, state) {
  const byStatus = {
    in_progress: state.tasks.filter((t) => t.status === 'in_progress'),
    pending: state.tasks.filter((t) => t.status === 'pending'),
    done: state.tasks.filter((t) => t.status === 'done'),
    cancelled: state.tasks.filter((t) => t.status === 'cancelled'),
  };

  const fmtLine = (t) => {
    const branches = t.branches.length ? ` _[${t.branches.join(', ')}]_` : '';
    return `- **${t.id}** ${t.title}${branches}`;
  };

  const sections = [
    '# Tasks',
    '',
    `_Last updated: ${new Date().toISOString()}_`,
    `_Auto-source: \`.context-compact/task-state.json\`. Do not edit this file directly — use \`storm task\` commands._`,
    '',
    '## In progress',
    byStatus.in_progress.length ? byStatus.in_progress.map(fmtLine).join('\n') : '_None_',
    '',
    '## Pending',
    byStatus.pending.length ? byStatus.pending.map(fmtLine).join('\n') : '_None_',
    '',
    '## Done',
    byStatus.done.length ? byStatus.done.map(fmtLine).join('\n') : '_None_',
    '',
    '## Cancelled',
    byStatus.cancelled.length ? byStatus.cancelled.map(fmtLine).join('\n') : '_None_',
    '',
  ];

  await writeFile(path.join(projectRoot, TASKS_FILE), sections.join('\n'), 'utf8');
}

